    private void DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteActionPerformed
        // TODO add your handling code here:
        
        // delete button to delete one row
        //if the user click witout any selected rows --> show message "Please , Select Row !!"

        /* Connection to delet Row*/
        int selectedrow = jTable2.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();

        Connection con = null;
        Statement stmt = null;

        try {

            con = DriverManager.getConnection(url);
            stmt = con.createStatement();

            String value = (jTable2.getModel().getValueAt(selectedrow, 0).toString());

            String query = "DELETE FROM Drugs WHERE Dr_id='" + value + "'";

            int affectedRows = stmt.executeUpdate(query);
            if (affectedRows == 1) {
                JOptionPane.showMessageDialog(this, "Successful Deleted !!!");
            } else {
                JOptionPane.showMessageDialog(this, "it is not match , please Try again!!!");
            }

            model.removeRow(jTable2.getSelectedRow());
        } catch (Exception ex) {
            JOptionPane.showConfirmDialog(this, ex.getMessage());
        } finally {
            if (con != null) {
                try {
                    con.close();
                } catch (Exception ex) {
                }
            }
            if (stmt != null) {
                try {
                    stmt.close();
                } catch (Exception ex) {
                }
            }
        }
           
    }//GEN-LAST:event_DeleteActionPerformed

