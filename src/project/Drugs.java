        public boolean insert(String id, String name, String type, String date,int quantity, double factP, double salesP) {
        try {

            con = DriverManager.getConnection(url);
            stmt = con.createStatement();
            String query = "insert into Drugs values('" + id + "','" + name + "','" + type + "'," + quantity + "," + factP + "," + salesP + ",'" + date + "')";

            int result = stmt.executeUpdate(query);

            if (result > 0) {

                return true;
            } else {
                return false;
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
            return false;
        } finally {
            if (con != null) {
                try {
                    con.close();
                } catch (Exception ex) {
                }
            }
            if (stmt != null) {
                try {
                    stmt.close();
                } catch (Exception ex) {
                }
            }
        }

    }    
        
    

